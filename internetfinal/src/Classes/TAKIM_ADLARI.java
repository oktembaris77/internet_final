package Classes;

public class TAKIM_ADLARI {
	private String takim1Adi;
	private String takim2Adi;
	public TAKIM_ADLARI() {
		
	}
	
	public TAKIM_ADLARI(String takim1Adi, String takim2Adi) {
		this.takim1Adi = takim1Adi;
		this.takim2Adi = takim2Adi;
	}

	public String getTakim1Adi() {
		return takim1Adi;
	}

	public void setTakim1Adi(String takim1Adi) {
		this.takim1Adi = takim1Adi;
	}

	public String getTakim2Adi() {
		return takim2Adi;
	}

	public void setTakim2Adi(String takim2Adi) {
		this.takim2Adi = takim2Adi;
	}
}
